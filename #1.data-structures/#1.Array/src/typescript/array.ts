// * Array Unidimensional (Vetor)
const callList = ["Abner", "Bianca", "César", "Fernanda", "Júlio", "William"];

console.log(callList); // [ 'Abner', 'Bianca', 'César', 'Fernanda', 'Júlio', 'William' ]

console.log(callList[0]); // Abner

console.log(callList[5]); // William

// * Array Multidimensional (Matriz)
const seats = [
  ["Abner", "Bianca", "César"],
  ["Fernanda", "Júlio", "William"],
];

// Onde Júlio está sentado?
console.log(seats[1][1]); // Júlio

// Onde Bianca está sentada?
console.log(seats[0][1]);

// Onde William está sentado?
console.log(seats[1][2]);

